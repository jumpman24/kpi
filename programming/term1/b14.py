"""
Дано натуральне чотирицифрове число n.
З’ясувати, чи є воно паліндромом (таким, що читається однаково зліва направо та зправа наліво, наприклад, 1221).
"""

if __name__ == '__main__':
    print(__doc__)
    while True:
        s = input("Введіть щось: ")

        if s in ["", "quit", "exit", "close"]:
            break

        if s == s[::-1]:
            print("Це паліндром!\n")
        else:
            print("Це не паліндром!\n")
